package utils.d2p.elements;

public class GraphicalElementData {
    private int id;
    private int type;

    public GraphicalElementData(int id, int type) {
        this.id = id;
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
